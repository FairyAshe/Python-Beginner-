#Arithmetic Operators are used to perform mathematical operations on our code

#Subtraction
print(4-61)

#Addition
print(4+61)

#Multiplication
print(4*61)

#Division
print(4/61)

#Modulus = Remainder
print(4%61)

#Floor Division = Rounded Off
print(4//61)

#Exponent
print(4**61)

#CALCULATOR
FirstNumber = float(input("Enter first number: ")) #Casting
SecondNumber = float(input("Enter second number: "))#Casting
Total = FirstNumber + SecondNumber
print("Your total is " + str(Total))
print(str(FirstNumber) + " + " + str(SecondNumber) + " = " + str(Total))
